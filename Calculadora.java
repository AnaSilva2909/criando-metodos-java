public class Calculadora {

    public static void somar(int num1, int num2){

        int resultado =  num1 + num2;

        System.out.println("Reultado da soma : " + resultado);

    }

    public static void subtrair(int num1, int num2){

        int resultado =  num1 - num2;

        System.out.println("Reultado da subtrair : " + resultado);

    }

    public static void multiplicação(int num1, int num2){

        int resultado =  num1 * num2;

        System.out.println("Reultado da multiplicação : " + resultado);

    }

    public static void divisão(int num1, int num2){

        int resultado =  num1 / num2;

        System.out.println("Reultado da divisão : " + resultado);

    }
}
